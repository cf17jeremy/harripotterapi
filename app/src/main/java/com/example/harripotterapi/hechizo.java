public class hechizo {
    public String hechizo;
    public String uso;

    public hechizo(String hechizo, String uso) {
        this.hechizo = hechizo;
        this.uso = uso;
    }

    public String getHechizo() {
        return hechizo;
    }

    public String getUso() {
        return uso;
    }
}
